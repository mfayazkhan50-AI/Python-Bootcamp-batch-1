# 7 = 7 -> assignment operator, not a comparison operator. 

# 7 == 7 -> equality operator, checks if the values are equal.

# Equal
print('Equal')
print(7 == 5)

# Not Equal
print('Not Equal')
print(7 != 7)

# Greater than
print('Greater than')
print(7 > 6)

# Less than
print('Less than')
print(7 < 8)

# Greater than or equal to
print('Greater than or equal to')
print(7 >= 1)

# Less than or equal to
print('Less than or equal to')
print(7 <= 7)