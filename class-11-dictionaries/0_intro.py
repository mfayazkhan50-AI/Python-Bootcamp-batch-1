'''
A Python dictionary is a built-in data structure that stores information in key-value pairs.
Instead of using numbers (like 0, 1, 2) to find your data, you use custom labels called keys to look up their matching values.
'''

'''
1. list [1, 2]
2. tuples (1, 2)
3. sets {1, 2}
4. dictionary {key: value}

'''

